/* Localize every published post's WordPress featured image and emit a
   slug -> local path map.

   The Payload import brought over post bodies but not featured images (the
   media table came across empty), so blog cards had no thumbnail. The WXR
   export still has them: postmeta _thumbnail_id -> attachment wp:attachment_url.

   Writes:
     public/wp-content/uploads/<file>          (downloaded, skipped if present)
     src/data/postThumbnails.ts                (slug -> local path)

   Usage: node scripts/fetch-post-thumbnails.mjs <export.xml>
   Deliberately a build-time script, not a migration: no Supabase change. */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'fs';
import { join } from 'path';

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

const xmlPath = process.argv[2];
if (!xmlPath) {
  console.error('usage: node scripts/fetch-post-thumbnails.mjs <export.xml>');
  process.exit(1);
}

const xml = readFileSync(xmlPath, 'utf8');
const items = xml.split('<item>').slice(1).map((r) => r.split('</item>')[0]);

const tag = (s, t) => {
  const m = s.match(new RegExp(`<${t}>([\\s\\S]*?)</${t}>`));
  if (!m) return null;
  return m[1].replace(/^<!\[CDATA\[/, '').replace(/\]\]>$/, '').trim();
};
const metaValue = (s, key) => {
  const re = new RegExp(
    `<wp:postmeta>\\s*<wp:meta_key>(?:<!\\[CDATA\\[)?${key}(?:\\]\\]>)?</wp:meta_key>\\s*<wp:meta_value>(?:<!\\[CDATA\\[)?([\\s\\S]*?)(?:\\]\\]>)?</wp:meta_value>`,
  );
  const m = s.match(re);
  return m ? m[1].trim() : null;
};

const attachments = new Map();
for (const it of items) {
  if (tag(it, 'wp:post_type') !== 'attachment') continue;
  const id = tag(it, 'wp:post_id');
  const url = tag(it, 'wp:attachment_url');
  if (id && url) attachments.set(id, url);
}

const posts = [];
for (const it of items) {
  if (tag(it, 'wp:post_type') !== 'post' || tag(it, 'wp:status') !== 'publish') continue;
  const thumbId = metaValue(it, '_thumbnail_id');
  const url = thumbId ? attachments.get(thumbId) : null;
  if (url) posts.push({ slug: tag(it, 'wp:post_name'), url });
}

const uploadsDir = join('public', 'wp-content', 'uploads');
mkdirSync(uploadsDir, { recursive: true });

const map = {};
let downloaded = 0;
let skipped = 0;
const failed = [];

for (const [i, post] of posts.entries()) {
  const filename = decodeURIComponent(post.url.split('/').pop().split('?')[0]);
  const localPath = `/wp-content/uploads/${filename}`;
  const diskPath = join(uploadsDir, filename);

  if (existsSync(diskPath)) {
    skipped++;
    map[post.slug] = localPath;
    continue;
  }

  /* The origin rate-limits (429) at roughly 40 rapid requests, so go slowly and
     back off rather than hammering the client's production server. */
  let ok = false;
  for (let attempt = 0; attempt < 5 && !ok; attempt++) {
    if (attempt > 0) await sleep(2000 * 2 ** attempt);
    try {
      const res = await fetch(post.url, { headers: { 'user-agent': 'Mozilla/5.0' } });
      if (res.status === 429 || res.status >= 500) continue;
      if (!res.ok) {
        failed.push({ slug: post.slug, url: post.url, status: res.status });
        ok = true;
        break;
      }
      const buf = Buffer.from(await res.arrayBuffer());
      if (buf.length < 100) {
        failed.push({ slug: post.slug, url: post.url, status: 'suspiciously small' });
        ok = true;
        break;
      }
      writeFileSync(diskPath, buf);
      map[post.slug] = localPath;
      downloaded++;
      ok = true;
    } catch (err) {
      if (attempt === 4) failed.push({ slug: post.slug, url: post.url, status: String(err) });
    }
  }
  if (!ok) failed.push({ slug: post.slug, url: post.url, status: '429 after retries' });

  await sleep(400);
  if ((i + 1) % 25 === 0) console.log(`  ...${i + 1}/${posts.length}`);
}

const header = `/* Featured image per blog post, recovered from the WordPress export and
   localized under public/wp-content/uploads/.

   GENERATED by scripts/fetch-post-thumbnails.mjs — do not hand-edit; re-run the
   script against a fresh export instead. */

`;
const body =
  'export const postThumbnails: Record<string, string> = ' +
  JSON.stringify(map, null, 2).replace(/"([^"]+)":/g, "  '$1':").replace(/"/g, "'") +
  ';\n';
writeFileSync(join('src', 'data', 'postThumbnails.ts'), header + body);

console.log(`\nposts with a featured image : ${posts.length}`);
console.log(`downloaded                  : ${downloaded}`);
console.log(`already present             : ${skipped}`);
console.log(`mapped                      : ${Object.keys(map).length}`);
console.log(`failed                      : ${failed.length}`);
for (const f of failed.slice(0, 20)) console.log(`  ${f.status}  ${f.slug}  ${f.url}`);
